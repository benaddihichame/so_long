#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;
	int	i2;

	i2 = 0;
	while (s[i2])
		i2++;
	i = i2;
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)s + i);
		i--;
	}
	return (NULL);
}
